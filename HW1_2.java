package Java.HW1;

public class HW1_2 {
    public static void main(String[] args) {
        System.out.println("Max Byte = " + Byte.MAX_VALUE);
        System.out.println("Min Byte = " + Byte.MIN_VALUE + "\n");
        System.out.println("Max Short = " + Short.MAX_VALUE);
        System.out.println("Min Short = " + Short.MIN_VALUE + "\n");
        System.out.println("Max Int = " + Integer.MAX_VALUE);
        System.out.println("Min Int = " + Integer.MIN_VALUE + "\n");
        System.out.println("Max Long = " + Long.MAX_VALUE);
        System.out.println("Min Long = " + Long.MIN_VALUE + "\n");

        System.out.println("Max Float = " + Float.MAX_VALUE);
        System.out.println("Min Float = " + Float.MIN_VALUE + "\n");
        System.out.println("Max Double = " + Double.MAX_VALUE);
        System.out.println("Min Double = " + Double.MIN_VALUE + "\n");

        System.out.println("Max Boolean = " + Boolean.FALSE);
        System.out.println("Min Boolean = " + Boolean.TRUE + "\n");
        System.out.println("Max Char = " + (Character.MAX_VALUE + 0));
        System.out.println("Min Char = " + (Character.MIN_VALUE + 0));
    }
}
